package javaprogramming;

public class relationallogicaloperatortask3{
	public static void main(String[]args) {
		int a=10;
		int b=20;
		
		boolean x=true;
		boolean y=true;
		
		
		//relational operator
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a==b);
		System.out.println(a!=b);
		//logical operator
		System.out.println(x&&y);
		System.out.println(x||y);
		System.out.println(!x);
		System.out.println(!y);
		
		
	}

}
